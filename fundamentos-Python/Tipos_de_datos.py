# Notas:
# En python no es necesario declarar el tipo de dato de la variable
# Algunos de los tipos de datos que se ocupan son, int,float,string,bolean.

# Ejemplos de tipos de datos.
# Número entero
mi_variable_entera = -100 #Declaracion de una variable de tipo entero
print("Tipo de dato entero:",mi_variable_entera) #Imprime el valor de la variable y un letrero adicional.

# Número decimal
mi_variable_decimal = 12.12 #Declaración de una variable tipo flotante
print("Tipo de dato decimal:", mi_variable_decimal) #Imprime el valo de la variable y un letrero adicional

# Cadena de texto
mi_variable_texto_nombre = "Rosalinda" #Declaración de una variable tipo string
mi_variable_texto_apellido = 'Aquino' #Declaración de una variable tipo string
print("Cadena de texto:", mi_variable_texto_nombre, mi_variable_texto_apellido) #Imprime el valor que continen las dos variables antes declaradas y un letrero adicional.

# Booleno
mi_variable_booleana = True #Declara una variable de tipo boleana.
print('Tipo booleano:', mi_variable_booleana)

# None
mi_variable_none = None #Declara una variable de tipo nulo
print("Tipo none:",mi_variable_none)
# Uso de constantes.
VARIABLE_CONSTANTE = 3.1416 #Se declaro una variable constante que no puede modificarse
print("Ejemplo de convención de una constante:", VARIABLE_CONSTANTE)